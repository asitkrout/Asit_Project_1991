package mq.java.variables;

public class Variables_With_Conditions {

	public static void main(String[] args) 
	{
		
		
		
		if (true)
		{
			int a=200;	
		} 
		
		
		int b;
		if(true)
		{
			b=100;
		
		}
		System.out.println(b);
		
		
		
		int age=10;
		boolean flag=false;
		if(age>12)
		{
			flag=true;
		}
		else
		{
			flag=false;
		}
		System.out.println(flag);
		
		

	}

}
