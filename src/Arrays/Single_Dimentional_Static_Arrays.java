package Arrays;

public class Single_Dimentional_Static_Arrays {

	public static void main(String[] args) 
	{
		String Browsers[]=new String[4];
		Browsers[0]="chrome";
		Browsers[1]="opera";
		Browsers[2]="safari";
		Browsers[3]="firefox";
		
		System.out.println(Browsers[2]);
		System.out.println(Browsers.length);
		
		int[] Num=new int[5];
		Num[0]=100;
		Num[1]=200;
		Num[2]=300;
		Num[3]=400;
		Num[4]=500;
		
		System.out.println(Num.length);
		
		Object Obj[]=new Object[5];
		Obj[0]="Iphone";
		Obj[1]=200;
		Obj[2]=5;
		Obj[3]=5000;
		Obj[4]="asit";
		
		System.out.println(Obj[0]);
		
		
		
		
		
		
	}

}
